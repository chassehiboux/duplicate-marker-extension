def get_downloads_root() -> Path:
    """
    Возвращает текущую папку для сохранения отчетов.
    Приоритет: 1. Сохраненная настройка 2. Сетевой путь по умолчанию.
    """
    if USER_SETTINGS_FILE.exists():
        try:
            data = json.loads(USER_SETTINGS_FILE.read_text(encoding='utf-8'))
            custom_path = data.get("download_path")
            if custom_path:
                path_obj = Path(custom_path)
                # Если папка существует или мы можем её создать
                return path_obj
        except Exception:
            pass
    
    # Fallback (по умолчанию)
    return DEFAULT_DOWNLOADS_ROOT

def set_custom_downloads_dir(path: str):
    """Сохраняет выбранный путь в настройки."""
    data = {}
    if USER_SETTINGS_FILE.exists():
        try:
            data = json.loads(USER_SETTINGS_FILE.read_text(encoding='utf-8'))
        except:
            pass
    
    data["download_path"] = str(path)
    USER_SETTINGS_FILE.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding='utf-8')

