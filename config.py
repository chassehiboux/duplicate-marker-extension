def get_user_local_dir() -> Path:
    """Возвращает путь к локальной папке приложения."""
    local_app_data = os.environ.get('LOCALAPPDATA')
    if local_app_data:
        local_dir = Path(local_app_data) / "PyramidReports"
    else:
        local_dir = Path.home() / ".PyramidReports"

    try:
        local_dir.mkdir(parents=True, exist_ok=True)
        (local_dir / "pw_user_data").mkdir(exist_ok=True)
        (local_dir / "tmp_downloads").mkdir(exist_ok=True)
    except Exception:
        pass
    return local_dir

def get_user_downloads_dir() -> Path:
    """Возвращает стандартную папку Загрузки."""
    return Path.home() / "Downloads"

